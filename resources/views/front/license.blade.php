                    <div>
                        <h2 class="text-xl font-semibold text-yellow-700 mb-3">سابع عشر: إخلاء المسئولية</h2>
                        <p class="mb-2">(1) أنت توافق على إخلاء مسئولية الشركة وموظفيها والأشخاص التابعين لها عن استخدام
                            البرنامج أو عدم قدرة المستخدم على استخدامه بالشكل الأمثل أو عن أي توقف أو أعطال أو فيروسات تصيب
                            البرنامج نتيجة ضعف الحماية وعن أي خسارة في الأرباح أو العائدات أو المبيعات أو البيانات أو تكاليف

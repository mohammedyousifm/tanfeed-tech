                            آخر فيجب أن يتم ذلك من خلال الشركة وأن توافق كتابة على نقل التراخيص، وفي هذه الحالة يحظر على
                            المستخدم استخدام البرنامج من وقت نقل الترخيص.</p>
                        <p>(6) <strong>ترخيص غير حصري:</strong> يعلم المستخدم ويوافق على أن الترخيص المقدم له من الشركة غير
                            حصري، وبالتالي يحق للشركة إعادة الترخيص للغير باستخدام البرنامج وفق الشروط التي تحددها.</p>
                    </div>

                    <!-- تاسعًا: التحديثات -->
                    <div>
                        <h2 class="text-xl font-semibold text-yellow-700 mb-3">تاسعًا: التحديثات</h2>
                        <p>
                            لا يحق للمستخدم عمل تحديثات للبرنامج أو تعديله أو تطويره بأي شكل من الأشكال، ويكون من حق الشركة
                            وحدها عمل التحديثات والتطويرات للبرنامج، ولا يشمل سعر البرنامج الحالي إجراء التحديثات، وفي حالة
                            طلبها من جانب المستخدم فإنه يلتزم بدفع مقابل جديد مقابل تزويده بالتحديثات المطلوبة.
                        </p>
                    </div>

                    <!-- عاشرًا: الدعم الفني -->
                    <div>
                        <h2 class="text-xl font-semibold text-yellow-700 mb-3">عاشرًا: الدعم الفني</h2>
                        <p class="mb-2">(1) في حالة طلب الدعم الفني من جانب المستخدم، يجب عليه التواصل مع الشركة لتحديد موعد
                            تقديم الدعم وتكلفته.</p>
                        <p>(2) في حالة قيام المستخدم بالدعم الفني بنفسه أو الاستعانة بالغير لتقديمه فيجب ألا يؤثر ذلك على
                            أحكام هذه الاتفاقية، فيحظر التلاعب في البرنامج أو شيفرة المصدر الخاصة به.</p>
                    </div>

                    <!-- حادي عشر: حقوق التأليف والنشر -->
                    <div>
                        <h2 class="text-xl font-semibold text-yellow-700 mb-3">حادي عشر: حقوق التأليف والنشر</h2>
                        <p class="mb-2">(1) البرنامج والأفكار المُعبَر عنها داخله، هي حقوق ملكية فكرية خالصة لنا، وأي تقليد
                            أو اقتباس للبرنامج أو بعض خدماتها (بما في ذلك الأفكار والنصوص والرموز والبرمجيات) يعد انتهاكًا
                            لحقوق التأليف والنشر الخاصة بنا، وسنقوم معه باتخاذ كافة الإجراءات القانونية ضد مرتكب الانتهاكات
                            المذكورة.</p>
                        <p class="mb-2">(2) جميع المحتويات المضمنة أو المتاحة ضمن خدمات البرنامج مثل (النصوص والشعارات
                            والصور والرسوم البيانية والتسجيلات الصوتية وأيقونات الأزرار والمحتويات الرقمية والمواد التي يتم
                            إعادة تحميلها والبرمجيات وتجميع البيانات) هي ملك للشركة، وهي محمية من قِبل القوانين السعودية
                            وقوانين حقوق التأليف والنشر الدولية.</p>
                        <p class="mb-2">(3) تجميع كافة البيانات المدرجة في خدمة البرنامج هو ملك حصري وخاص بالشركة ومحمية
                            بموجب قوانين حقوق الطبع والنشر السعودية والدولية وكذلك محمية بموجب الاتفاقيات الدولية السارية،
                            مثل اتفاقية برن، واتفاقية تربس.</p>
                        <p class="mb-2">(4) لا يجوز لك نسخ البرنامج باستثناء ما هو منصوص عليه في القسم (خامس عشر/5).</p>
                        <p>(5) يجب أن تحتوي أي نُسخ يُسمح لك بعملها بموجب هذه الاتفاقية على نفس حقوق الطبع والنشر وإشعارات
                            الملكية الأخرى التي تظهر بالبرنامج.</p>
                    </div>


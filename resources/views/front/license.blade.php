                    <!-- رابع عشر: الأطراف الثالثة -->
                    <div>
                        <h2 class="text-xl font-semibold text-yellow-700 mb-3">رابع عشر: الأطراف الثالثة</h2>
                        <p class="mb-2">(1) تحتوي خدمات البرنامج على روابط ومحتوى متوفر على مواقع إنترنت أخرى وهي مواقع
                            خارجية يتم تشغيلها من جانب أطراف ثالثة، وأنت تقر بأن الشركة غير مسؤولة عن توفير أي روابط أو
                            المحتوى المتاح من خلالها، وبالتالي قد يتم إزالة الروابط أو حذف أو تغيير المحتوى وفقًا لرؤية هذه
                            المواقع الخارجية.</p>
                        <p class="mb-2">(2) يخضع استخدامك لمواقع الأطراف الثالثة إلى الشروط والأحكام وسياسة الخصوصية التي
                            تعتمدها هذه المواقع، ولا تقدم الشركة أي ضمانات بشأن قانونية هذه الشروط والسياسات أو مدى التزام
                            هذه المواقع والأطراف الثالثة بتنفيذها بشكل قانوني.</p>
                        <p class="mb-2">(3) لا يعد عرض روابط الأطراف الثالثة أو المحتوى الخاص بها اعتمادًا من الشركة لهذه
                            الروابط أو المحتوى، وبالتالي لا نضمن قانونية المحتوى أو التزام الشركة بالوفاء بالالتزامات
                            الواردة فيه، كما لا نضمن خلو هذه المواقع أو الروابط من الفيروسات أو البرمجيات الضارة.</p>
                        <p>(4) إذا كان لديك أية مخاوف أو استفسارات بشأن روابط أو محتوى الأطراف الثالثة أو شروطهم وسياساتهم
                            فيجب عليك الاتصال بالمسئول عن هذه المواقع وطلب الاستفسارات منه مباشرة.</p>
                    </div>

                    <!-- خامس عشر: سياسة الاستخدام المقبول -->
                    <div>
                        <h2 class="text-xl font-semibold text-yellow-700 mb-3">خامس عشر: سياسة الاستخدام المقبول</h2>
                        <p class="mb-2">(1) بالتزامك بشروط الاستخدام أو أي شروط خدمة وكذلك بدفعك لأي رسوم مطبقة، تمنحك
                            الشركة ترخيصًا محدودًا وغير حصري، وغير قابل للتحويل وغير قابل للترخيص من الباطن للوصول إلى
                            الاستخدام غير التجاري لخدمات البرنامج.</p>
                        <p class="mb-2">(2) لا يشمل هذا الترخيص إعادة بيع أو أي استخدام تجاري لأي من خدماتنا أو محتوياتها،
                            كما لا يشمل أي نسخ للمعلومات المتوفرة عن البرنامج لصالح الغير، أو أي استخدام لوسائل استخراج
                            البيانات أو أي استخدام لأدوات جمع واستخلاص البيانات المماثلة.</p>
                        <p class="mb-2">(3) لا يجوز لك إعادة إنتاج أو عمل نسخة مطابقة لهذا البرنامج، أو نسخ أو بيع أو إعادة
                            بيع أي جزء منه، أو استخدامه بصورة مغايرة في أغراض الاستغلال التجاري أو غير التجاري له دون الحصول
                            على موافقة كتابية صريحة من الشركة.</p>
                        <p class="mb-2">(4) لا يجوز لك استخدام أي علامات وصفية أو أي نص آخر يستغل اسم الشركة أو علاماتها
                            التجارية بدون موافقة كتابية صريحة من الشركة. كما لا يحق لك نسخ أو توزيع البرنامج أو استخراج أي
                            مكونات منه أو القيام بأعمال مشتقة من البرنامج.</p>
                        <p class="mb-2">(5) يحق لك عمل نسخة واحدة احتياطية من البرنامج على وسيط تخزين كنسخة احتياطية
                            أرشيفية، بشرط ألا تكون النسخة الاحتياطية مثبتة أو مستخدمة على أي جهاز حاسب آلي، وأي نسخ أخرى يتم
                            إعدادها من البرنامج تشكل انتهاكًا لهذه الاتفاقية.</p>
                        <p class="mb-2">(6) لا يحق لك استخدام البرنامج أو تعديله أو ترجمته أو إعادة إنتاجه أو نقل حقوق
                            استخدام البرنامج أو نسخ البرنامج بأي طريقة بخلاف ما هو منصوص عليه في هذه الاتفاقية.</p>
                        <p class="mb-2">(7) لا يحق لك ترخيص البرنامج من الباطن أو تأجيره أو استئجاره أو إعارته، أو استخدام
                            البرنامج لتوفير خدمات تجارية.</p>
                        <p class="mb-2">(8) لا يحق لك عمل هندسة عكسية للبرنامج، أو عكس تجميعه أو تفكيكه، أو محاولة اكتشاف
                            شيفرة المصدر الخاصة بالبرنامج.</p>
                        <p class="mb-2">(9) أنت توافق على عدم استخدام البرنامج إلا بطريقة تتوافق مع القوانين المعمول بها،
                            بما في ذلك على سبيل المثال لا الحصر القيود المطبقة على حقوق النشر وحقوق الملكية الفكرية الأخرى.
                            وتوافق على عدم استخدام البرنامج أو أي خدمة تقدم من خلاله بطريقة غير قانونية أو احتيالية أو
                            معادية للمجتمع على النحو الذي نقدره.</p>
                        <p class="mb-2">(10) يجب أن تكون جميع المعلومات التي تفصح عنها حقيقية ومحدثة وصحيحة وتعبر عنك وتوافق
                            ما هو مطلوب في استمارة التسجيل لدينا.</p>
                        <p class="mb-2">(11) يتعهد المستخدم بتحري الدقة عند إدخال المعلومات المطلوب إدخالها في البرنامج،
                            ويكون مسئولاً عن مراجعة تلك المعلومات بشكل دوري بغرض تصحيحها أو تعديلها أو تجديدها كلما توفرت
                            لديك معلومات جديدة بشأنها، على أن تحتفظ بكافة البيانات والمستندات الدالة على ذلك.</p>
                        <p class="mb-2">(12) إذا قدمت معلومات غير صحيحة أو غير دقيقة أو غير متزامنة أو غير كاملة، أو إذا كان
                            لدينا أسباب معقولة للاشتباه في أن هذه المعلومات غير صحيحة أو غير دقيقة أو غير متزامنة أو غير
                            كاملة أو لا تتوافق مع اتفاقية الاستخدام هذه، ودون الإخلال بأي حقوق أخرى بموجب هذه الاتفاقية أو
                            القانون، سوف نقوم بتعليق عضويتك أو إلغائها.</p>
                        <p class="mb-2">(13) يتعهد المستخدم بالمحافظة على المعلومات السرية الخاصة بحسابه، مثل اسم المستخدم
                            وكلمة المرور، وأنه مسئول عن أي إفصاح للغير عن هذه المعلومات، كما أنه مسئول عن أي استخدام يقع من
                            أي شخص قام بالإفصاح له عن هذه المعلومات السرية.</p>
                        <p class="mb-2">(14) يتعهد المستخدم بإبلاغ الشركة فورًا في حالة اختراق أو سرقة الحساب أو اكتشاف أي
                            استخدام غير قانوني لحسابه بالبرنامج، وذلك حتى نستطيع اتخاذ الإجراءات التقنية اللازمة للمحافظة
                            على الحساب.</p>
                        <p class="mb-2">(15) يحظر على المستخدم استخدام حساب أي شخص آخر مسجل في البرنامج في أي وقت أو لأي سبب
                            من الأسباب دون الحصول على موافقة صريحة من الشركة.</p>
                        <p class="mb-2">(16) يقر المستخدم بأن البيانات المقدمة منه من خلال الشركة صحيحة ودقيقة وخاصة به،
                            ويتحمل أي مسئولية قانونية ناشئة عن أي بيانات كاذبة أو مضللة أو خاصة بالغير يقدمها من خلال
                            الشركة.</p>
                        <p class="mb-2">(17) يحظر على المستخدم التسجيل لدينا بأكثر من حساب في البرنامج، كما يحظر على
                            المستخدم التسجيل لدينا مرة أخرى في حالة حظر عضويته دون الحصول على موافقتنا، وفي حال اكتشاف
                            الشركة ذلك يحق لنا حظر العضوية مع اتخاذ الإجراءات المناسبة.</p>
                        <p class="mb-2">(18) جميع الحقوق غير الممنوحة لك صراحة في شروط الاستخدام هذه أو أي شروط خدمة أخرى
                            تحتفظ بها الشركة. وتنتهي التراخيص الممنوحة من قِبلنا إذا لم تلتزم بشروط الاستخدام هذه أو أي شروط
                            خدمة أخرى.</p>
                        <p>(19) في حالة إلغاء الترخيص يجب على الفور حذف البرنامج وجميع النسخ الاحتياطية أو إتلافها أو
                            إرجاعها على نفقتك الخاصة إلى الشركة.</p>
                    </div>

                    <!-- سادس عشر: المسئولية القانونية -->
                    <div>
                        <h2 class="text-xl font-semibold text-yellow-700 mb-3">سادس عشر: المسئولية القانونية</h2>
                        <p class="mb-2">(1) يجب عليك الالتزام بجميع القوانين واللوائح المعمول بها داخل الدولة التي تستخدم
                            البرنامج من خلالها وذلك فيما يتعلق باستخدامك للبرنامج، وتتحمل كافة المسئوليات الناشئة في حالة
                            إخلالك بهذه القوانين أو اللوائح، كما تلتزم بجميع الشروط والأحكام المنصوص عليها في هذه الاتفاقية.
                        </p>
                        <p class="mb-2">(2) أنت توافق صراحة على أنك تستخدم البرنامج على مسئوليتك الشخصية، وتكون مسؤول عن
                            المحافظة على استخدام البرنامج بكل جدية ومصداقية وتكون ملزمًا بالتعويض عن أي خسائر أو أضرار قد
                            تلحق بالشركة نتيجة أي استخدام غير شرعي أو غير مفوض من قِبلنا.</p>
                        <p class="mb-2">(3) يزودنا المستخدم بأي بيانات أو محتويات مضمنة بالبرنامج على مسئوليته الشخصية،
                            وبالتالي يتحمل المسئولية القانونية وحده إذا كانت هذه البيانات والمحتويات تنتهك حقوق الملكية
                            الفكرية أو العلامات التجارية الخاصة بالغير، أو إذا كانت تنتهك حقوق الخصوصية أو الأسرار التجارية
                            أو أي حقوق أخرى للغير.</p>
                        <p class="mb-2">(4) أنت توافق على تعويض الشركة وإبراء مسئوليتها والدفاع عنها ضد كافة الدعاوى
                            والمطالبات التي قد يرفعها أو يطالب بها الغير نتيجة استخدامك للبرنامج، أو بسبب قيامك بانتهاك هذه
                            الشروط والأحكام.</p>
                        <p class="mb-2">(5) تقع مسئولية استخدام أو الاعتماد على المعلومات التي يتلقاها أو يتوصل إليها
                            المستخدم من خلال خدمات البرنامج على عاتق هذا المستخدم بشكل كامل.</p>
                        <p class="mb-2">(6) لا تضمن الشركة اتخاذها إجراءات ضد كل الانتهاكات التي قد تحدث لاتفاقية الاستخدام
                            هذه، ولا يعني عدم اتخاذنا للإجراءات القانونية في أي من حالات الانتهاك تنازلاً عن حقنا في اتخاذ
                            هذه الإجراءات في أي وقت نراه مناسبًا.</p>
                        <p class="mb-2">(7) على المستخدم أن يحمي الشركة وموظفيها وأن يدافع عنهم ويعوضهم عن أية خسائر ناتجة
                            عن أية دعوى أو مطالبة تتعلق بالبرنامج أو ناتجة عن عمل أو إهمال من قِبل المستخدم أو ممثليه أو
                            وكلائه.</p>
                        <p>(8) أنت ملزمًا بالتعويض عن أي خسائر أو أضرار قد تلحق بالشركة نتيجة أي استخدام غير شرعي أو غير
                            مفوض من قِبلنا.</p>
                    </div>


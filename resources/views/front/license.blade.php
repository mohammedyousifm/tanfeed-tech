                    <div>
                        <h2 class="text-xl font-semibold text-yellow-700 mb-3">ثاني عشر: العلامات التجارية</h2>
                        <p class="mb-2">(1) "تنفيذ تك" والشعارات المرتبطة بها هي علامات تجارية و/أو علامات خدمة خاصة بنا.
                            والصور والشعارات ورؤوس الصفحات وأيقونات الأزرار والنصوص والأسماء الخدمية هي علامات تجارية
                            وتصميمات تجارية خاصة بالشركة.</p>
                        <p>(2) يحظر إعادة إنتاج العلامات أو التصميمات التجارية الخاصة بالبرنامج في أي وسيلة إعلامية أو
                            إعلانية دون إذن كتابي منا. ولا يجوز استخدام العلامات والتصميمات التجارية الخاصة بالبرنامج في
                            اتصال بأي منتج أو خدمة ليست تابعة لنا، حتى لا تتسبب في النيل من قدر ومصداقية الشركة أو النيل من
                            عملائها.</p>
                    </div>

                    <!-- ثالث عشر: الاحتفاظ بالحقوق -->
                    <div>
                        <h2 class="text-xl font-semibold text-yellow-700 mb-3">ثالث عشر: الاحتفاظ بالحقوق</h2>
                        <p class="mb-2">(1) تحتفظ الشركة بجميع حقوق البرنامج، باستثناء الحقوق الممنوحة لك صراحة بموجب شروط
                            هذه الاتفاقية بصفتك المستخدم النهائي للبرنامج.</p>
                        <p>(2) لا تنشئ هذه الاتفاقية أي التزامات على عاتق الشركة وحاملي تراخيصها بخلاف تلك المنصوص عليها في
                            هذه الاتفاقية.</p>
